    function shuffleWord(word) {
        do {
            var a = word.split(""),
            n = a.length;
            
            for(var i = n - 1; i > 0; i--) {
                var j = Math.floor(Math.random() * (i + 1));
                var tmp = a[i];
                a[i] = a[j];
                a[j] = tmp;
            }
        }
        while(a.join("") == word);
        return a.join("");
    }
    
    function shuffleSentence(sentence) {
        do {
            var a = sentence.split(" "),
            n = a.length;
            
            for(var i = n - 1; i > 0; i--) {
                var j = Math.floor(Math.random() * (i + 1));
                var tmp = a[i];
                a[i] = a[j];
                a[j] = tmp;
            }
        }
        while(a.join(" ") == sentence);
        return a.join("&nbsp;&nbsp;&nbsp;");
    }
    
